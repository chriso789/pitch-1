-- 3) Sync contacts.qualification_status and pipeline_entries.status by normalized name + address
WITH matches AS (
  SELECT c.id AS contact_id, r.qs, r.ps
  FROM contacts c
  JOIN _redline r
    ON regexp_replace(lower(trim(coalesce(c.first_name,'')||' '||coalesce(c.last_name,''))),'[^a-z0-9 ]','','g') = r.name
   AND regexp_replace(lower(trim(coalesce(c.address_street,''))),'[^a-z0-9 ]','','g') = r.addr
)
UPDATE contacts c
   SET qualification_status = m.qs,
       updated_at = now()
  FROM matches m
 WHERE c.id = m.contact_id
   AND (c.qualification_status IS DISTINCT FROM m.qs);

WITH matches AS (
  SELECT pe.id AS pe_id, r.ps
  FROM pipeline_entries pe
  JOIN contacts c ON c.id = pe.contact_id
  JOIN _redline r
    ON regexp_replace(lower(trim(coalesce(c.first_name,'')||' '||coalesce(c.last_name,''))),'[^a-z0-9 ]','','g') = r.name
   AND regexp_replace(lower(trim(coalesce(c.address_street,''))),'[^a-z0-9 ]','','g') = r.addr
  WHERE pe.deleted_at IS NULL
)
UPDATE pipeline_entries pe
   SET status = m.ps,
       updated_at = now()
  FROM matches m
 WHERE pe.id = m.pe_id
   AND (pe.status IS DISTINCT FROM m.ps)
   AND pe.status NOT IN ('project','completed','sold','closed','legal_review','claim_filed','claim_approved','capped_out');
